#pragma once

#include <Arduino.h>

// ============================================================
//  1. НАСТРОЙКИ КОНТРОЛЛЕРА (FT85BD)
// ============================================================

// UART пины для связи с контроллером FT85BD
#define FT85BD_RX_PIN       18   // ESP32 RX (подключить к TX контроллера)
#define FT85BD_TX_PIN       17   // ESP32 TX (подключить к RX контроллера)
#define FT85BD_BAUDRATE     115200

// ============================================================
//  2. НАСТРОЙКИ ДАТЧИКА СКОРОСТИ (Холл)
// ============================================================

#define SPEED_SENSOR_PIN        9    // GPIO для датчика Холла
#define MAGNETS_COUNT           6    // Количество магнитов на крышке мотор-колеса
#define WHEEL_DIAMETER_MM       559  // Диаметр колеса в мм (22" ≈ 558,8мм)

// Расчёт длины окружности колеса (в метрах)
#define WHEEL_CIRCUMFERENCE_M   ((WHEEL_DIAMETER_MM / 1000.0f) * 3.14159f)

// ============================================================
//  3. НАСТРОЙКИ БАТАРЕИ
// ============================================================

#define BATTERY_CELLS           13      // Количество банок (S) в батарее
#define BATTERY_CELL_MAX_VOLT   4.2f    // Максимальное напряжение одной банки
#define BATTERY_CELL_MIN_VOLT   3.0f    // Минимальное напряжение одной банки
#define BATTERY_CAPACITY_WH     960.0f  // Емкость батареи в Вт·ч

// Расчётные значения (не изменять)
#define BATTERY_MAX_VOLT        (BATTERY_CELLS * BATTERY_CELL_MAX_VOLT)
#define BATTERY_MIN_VOLT        (BATTERY_CELLS * BATTERY_CELL_MIN_VOLT)

// ============================================================
//  4. НАСТРОЙКИ ПЕРЕДАЧ (PAS MODE)
// ============================================================

// Токи для каждой передачи (А) — используются только для отображения
// Реальные ограничения задаются в ESC Tool
#define PAS_GEAR1_CURRENT       5.0f   // 1-я передача
#define PAS_GEAR2_CURRENT       10.0f  // 2-я передача
#define PAS_GEAR3_CURRENT       15.0f  // 3-я передача
#define PAS_GEAR4_CURRENT       20.0f  // 4-я передача
#define PAS_GEAR5_CURRENT       25.0f  // 5-я передача

// ============================================================
//  5. НАСТРОЙКИ КНОПОК УПРАВЛЕНИЯ
// ============================================================

#define BUTTON_GEAR_UP_PIN      2   // Кнопка "+" (передача вверх) Освобожденный пин звука. Снять резистор R21!
#define BUTTON_GEAR_DOWN_PIN    14   // Кнопка "-" (передача вниз / круиз)
#define DEBOUNCE_MS             100  // Антидребезг (мс)
#define LONG_PRESS_MS           500  // Время для долгого нажатия (мс)

// ============================================================
//  6. НАСТРОЙКИ SD-КАРТЫ
// ============================================================

#define SD_CS_PIN               10    // Chip Select
#define SD_SCK_PIN              12   // SPI SCK
#define SD_MOSI_PIN             11   // SPI MOSI
#define SD_MISO_PIN             13   // SPI MISO
#define SD_SPEED_MHZ            4    // Скорость SPI (4 МГц)

// ============================================================
//  7. НАСТРОЙКИ ДИСПЛЕЯ
// ============================================================

// Размеры дисплея
#define TFT_res_W               320
#define TFT_res_H               480
#define TFT_rot                 1     // Поворот: 0, 90, 180, 270

// Пины дисплея (QSPI)
#define TFT_CS                  45
#define TFT_SCK                 47
#define TFT_SDA0                21
#define TFT_SDA1                48
#define TFT_SDA2                40
#define TFT_SDA3                39

// Подсветка
#define TFT_BL                  1

// ============================================================
//  8. НАСТРОЙКИ СЕНСОРНОГО ЭКРАНА
// ============================================================

#define Touch_SCL               8
#define Touch_SDA               4
#define Touch_INT               3
#define Touch_ADDR              0x3B

// Калибровка сенсора
#define Touch_X_min 12
#define Touch_X_max 310
#define Touch_Y_min 14
#define Touch_Y_max 461

// ============================================================
//  9. НАСТРОЙКИ BLE
// ============================================================

#define BLE_DEVICE_NAME         "MINAKO Display"
#define BLE_MTU_SIZE            128

// ============================================================
//  10. НАСТРОЙКИ UI (Экраны)
// ============================================================

#define UI_MAIN_SCREEN          0
#define UI_SETTINGS_SCREEN      1

// ============================================================
//  11. НАСТРОЙКИ ЛОГИРОВАНИЯ
// ============================================================

#define ENABLE_SERIAL_LOG       1
#define LOG_LEVEL_DEBUG         1
#define LOG_LEVEL_INFO          1
#define LOG_LEVEL_WARN          1
#define LOG_LEVEL_ERROR         1

// ============================================================
//  12. ВРЕМЕННЫЕ НАСТРОЙКИ
// ============================================================

#define UART_UPDATE_INTERVAL_MS 100     // Интервал опроса UART (мс)
#define SPEED_UPDATE_INTERVAL_MS 100    // Интервал обновления скорости (мс)
#define UI_UPDATE_INTERVAL_MS   100     // Интервал обновления UI (мс)
#define BLE_UPDATE_INTERVAL_MS  50      // Интервал обновления BLE (мс)

// ============================================================
//  МАКРОСЫ ДЛЯ УДОБСТВА
// ============================================================

// Преобразование RPM в км/ч (используется в bike_math.cpp)
#define RPM_TO_KMPH(rpm, diameter_mm) \
    ((rpm) * 3.14159f * (diameter_mm) * 0.06f / 1000.0f)

// Преобразование напряжения в процент батареи
#define VOLTAGE_TO_PERCENT(voltage) \
    constrain(((voltage) - BATTERY_MIN_VOLT) / (BATTERY_MAX_VOLT - BATTERY_MIN_VOLT) * 100, 0, 100)

// ============================================================
//  ПРОВЕРКА КОРРЕКТНОСТИ НАСТРОЕК
// ============================================================

#if BATTERY_CELLS < 1 || BATTERY_CELLS > 20
#error "BATTERY_CELLS must be between 1 and 20"
#endif

#if WHEEL_DIAMETER_MM < 100 || WHEEL_DIAMETER_MM > 1000
#error "WHEEL_DIAMETER_MM must be between 100 and 1000 mm"
#endif

#if MAGNETS_COUNT < 5 || MAGNETS_COUNT > 12
#error "MAGNETS_COUNT must be between 5 and 12"
#endif