//    GPIO17 Разъем P3 (контакт 3) — готов к работе.
//    GPIO18 Разъем P3 (контакт 4) — готов к работе
//    GPIO9  Разъем P2 (контакт 7) — готов к работе.
//    GPIO14 Разъем P2 (контакт 8) — готов к работе.
//    GPIO2  Освобожденный пин звука. Снять резистор R21!
